#!/usr/bin/env node
import assert from "node:assert/strict";
import { existsSync } from "node:fs";
import { chmod, mkdir, mkdtemp, readFile, stat, writeFile } from "node:fs/promises";
import { dirname, join, resolve } from "node:path";
import { readCurrentRevisionSync } from "./lib/git-revision.mjs";

const SCAFFOLD_SCHEMA = "learning-companion.platform-qa-evidence-scaffold.v1";
const PLATFORM_QA_HANDOFF_SCHEMA = "learning-companion.platform-qa-handoff.v1";
const DEFAULT_PLATFORM_HANDOFF_PATH = ".codex-tmp/platform-qa-handoff/current.json";
const DEFAULT_OUT_PATH = ".codex-tmp/platform-qa-evidence/scaffold-summary.json";
const DEFAULT_MARKDOWN_OUT_PATH = ".codex-tmp/platform-qa-evidence/scaffold-summary.md";
const SCAFFOLD_EVIDENCE_TIER = "PLATFORM_QA_EVIDENCE_SCAFFOLD_ONLY";
const SAFE_EVIDENCE_ROOT_PREFIX = ".codex-tmp/platform-qa-evidence";

const args = parseArgs(process.argv.slice(2));
if (args.help) {
  console.log(buildHelp());
  process.exit(0);
}
if (args["self-test"]) {
  await runSelfTest();
  process.exit(0);
}
if (args["platform-handoff"] === true) throw new Error("--platform-handoff requires a JSON path.");
if (args.out === true) throw new Error("--out requires a JSON path.");
if (args["markdown-out"] === true) throw new Error("--markdown-out requires a Markdown path.");

const platformHandoffPath = String(args["platform-handoff"] || DEFAULT_PLATFORM_HANDOFF_PATH);
const outPath = String(args.out || DEFAULT_OUT_PATH);
const markdownOutPath = String(args["markdown-out"] || DEFAULT_MARKDOWN_OUT_PATH);
const scaffold = await buildPlatformQaEvidenceScaffold(platformHandoffPath);
await writePrivateFile(resolve(outPath), `${JSON.stringify(scaffold, null, 2)}\n`);
await writePrivateFile(resolve(markdownOutPath), buildScaffoldMarkdown(scaffold));
console.log(buildConsoleSummary(scaffold, outPath, markdownOutPath));

async function buildPlatformQaEvidenceScaffold(platformHandoffPath) {
  const handoff = await readJson(platformHandoffPath, "platform QA handoff");
  assert.equal(handoff.schema, PLATFORM_QA_HANDOFF_SCHEMA, "platform QA handoff schema");
  assert.equal(handoff.evidenceTier, "PLATFORM_QA_HANDOFF_ONLY", "platform QA handoff evidence tier");
  assert.equal(handoff.canClaimKo, false, "platform QA handoff must not claim KO");
  const currentRevision = readCurrentRevisionSync();
  const platformSummaries = [];
  for (const platform of Array.isArray(handoff.platforms) ? handoff.platforms : []) {
    platformSummaries.push(await scaffoldPlatform(platform));
  }
  return {
    schema: SCAFFOLD_SCHEMA,
    evidenceTier: SCAFFOLD_EVIDENCE_TIER,
    generatedAt: new Date().toISOString(),
    canClaimKo: false,
    canClaimPlatformQa: false,
    platformHandoffPath,
    handoffGitHead: handoff.currentRevision?.gitHead || "TBD",
    currentRevision,
    summary: summarizeScaffold(platformSummaries),
    platforms: platformSummaries,
    claimBoundary: {
      proves: [
        "Platform QA evidence directories and placeholder note templates were created or already existed.",
        "No platform QA result rows, receipts, or screenshots were generated by this scaffold."
      ],
      doesNotProve: [
        "A real Mac, Windows, or HarmonyOS QA row was executed.",
        "Any row result is PASS, FAIL, or BLOCKED.",
        "A real screenshot exists; this scaffold creates only screenshot TODO marker files.",
        "The next-major KO gate is claimable."
      ]
    }
  };
}

async function scaffoldPlatform(platform) {
  const root = String(platform.suggestedEvidenceRoot || "");
  assertSafeEvidencePath(root, `${platform.id || "platform"} suggestedEvidenceRoot`);
  const hints = platform.currentTemplateSummary?.rowEvidenceHints || [];
  assert.ok(Array.isArray(hints), `${platform.id || "platform"} rowEvidenceHints must be an array`);
  const rows = [];
  await mkdir(root, { recursive: true, mode: 0o700 });
  const readme = buildPlatformReadme(platform);
  const readmeResult = await writePrivateFileIfAbsent(join(root, "README.md"), readme);
  for (const hint of hints) {
    rows.push(await scaffoldRow(platform, hint));
  }
  return {
    id: platform.id || "UNKNOWN",
    label: platform.label || platform.id || "UNKNOWN",
    qaPath: platform.qaPath || "",
    suggestedEvidenceRoot: root,
    readmePath: join(root, "README.md"),
    readmeCreated: readmeResult.created,
    readmeSkippedExisting: readmeResult.skippedExisting,
    rowsExpected: platform.expectedRows || hints.length,
    rowsScaffolded: rows.length,
    notesCreated: rows.filter((row) => row.notesCreated).length,
    notesSkippedExisting: rows.filter((row) => row.notesSkippedExisting).length,
    screenshotTodoCreated: rows.filter((row) => row.screenshotTodoCreated).length,
    screenshotTodoSkippedExisting: rows.filter((row) => row.screenshotTodoSkippedExisting).length,
    rows
  };
}

async function scaffoldRow(platform, hint) {
  const row = Number(hint.row || 0);
  const area = String(hint.area || `row ${row || "unknown"}`);
  const evidenceDir = String(hint.evidenceDir || "");
  assert.ok(Number.isInteger(row) && row > 0, `${platform.id || "platform"} row hint must include a positive row number`);
  assertSafeEvidencePath(evidenceDir, `${platform.id || "platform"} row ${row} evidenceDir`);
  assertPathUnderSuggestedEvidenceRoot(
    evidenceDir,
    String(platform.suggestedEvidenceRoot || ""),
    `${platform.id || "platform"} row ${row} evidenceDir`
  );
  await mkdir(evidenceDir, { recursive: true, mode: 0o700 });
  const notesPath = join(evidenceDir, "notes.md");
  const screenshotTodoPath = join(evidenceDir, "screenshot.png.TODO.txt");
  const notesResult = await writePrivateFileIfAbsent(notesPath, buildRowNotesTemplate(platform, hint));
  const screenshotTodoResult = await writePrivateFileIfAbsent(screenshotTodoPath, buildScreenshotTodo(platform, hint));
  return {
    row,
    area,
    evidenceDir,
    notesPath,
    notesCreated: notesResult.created,
    notesSkippedExisting: notesResult.skippedExisting,
    screenshotTodoPath,
    screenshotTodoCreated: screenshotTodoResult.created,
    screenshotTodoSkippedExisting: screenshotTodoResult.skippedExisting,
    screenshotPath: join(evidenceDir, "screenshot.png"),
    screenshotCreated: false
  };
}

function summarizeScaffold(platforms) {
  const rows = platforms.flatMap((platform) => platform.rows);
  return {
    platforms: platforms.length,
    rows: rows.length,
    notesCreated: rows.filter((row) => row.notesCreated).length,
    notesSkippedExisting: rows.filter((row) => row.notesSkippedExisting).length,
    screenshotTodoCreated: rows.filter((row) => row.screenshotTodoCreated).length,
    screenshotTodoSkippedExisting: rows.filter((row) => row.screenshotTodoSkippedExisting).length,
    screenshotsCreated: rows.filter((row) => row.screenshotCreated).length
  };
}

function buildPlatformReadme(platform) {
  return [
    `# ${platform.label || platform.id || "Platform"} QA Evidence Scaffold`,
    "",
    "TEMPLATE ONLY - not QA evidence.",
    "",
    `Platform ID: ${platform.id || "UNKNOWN"}`,
    `QA template: ${platform.qaPath || "TBD"}`,
    `Expected rows: ${platform.expectedRows || "TBD"}`,
    "",
    "Use this directory only during a real named-platform QA run. Replace each row notes.md with observed facts, and save a real screenshot as screenshot.png only after the row was actually executed.",
    ""
  ].join("\n");
}

function buildRowNotesTemplate(platform, hint) {
  return [
    `# Row ${hint.row}: ${hint.area || "TBD"}`,
    "",
    "TEMPLATE ONLY - replace before use. This file is not QA evidence.",
    "",
    `Platform: ${platform.label || platform.id || "TBD"}`,
    `QA template: ${platform.qaPath || "TBD"}`,
    `Evidence directory: ${hint.evidenceDir || "TBD"}`,
    "",
    "## Actual result",
    "",
    "- Result: <actual-result>",
    "- Observed summary: <observed-summary>",
    "- Reviewer: <reviewer>",
    "- Date/time: <ISO-8601-with-timezone>",
    "- Device/build/browser: <actual-environment>",
    "",
    "## Evidence",
    "",
    "- Screenshot: screenshot.png",
    "- Additional logs/files: <paths-or-none>",
    "",
    "Do not copy this template into a QA Notes cell until every placeholder above is replaced with real observations from the named platform.",
    ""
  ].join("\n");
}

function buildScreenshotTodo(platform, hint) {
  return [
    "This is not a screenshot.",
    `Platform: ${platform.id || "UNKNOWN"}`,
    `Row: ${hint.row || "TBD"} ${hint.area || ""}`.trim(),
    "",
    "During a real platform QA run, save the actual captured screenshot as screenshot.png in this directory.",
    "Do not rename this TODO marker to screenshot.png.",
    ""
  ].join("\n");
}

function buildScaffoldMarkdown(scaffold) {
  const lines = [
    "# Platform QA Evidence Scaffold",
    "",
    `Evidence tier: ${scaffold.evidenceTier}`,
    `Can claim KO: ${scaffold.canClaimKo}`,
    `Can claim platform QA: ${scaffold.canClaimPlatformQa}`,
    `Handoff git HEAD: ${scaffold.handoffGitHead}`,
    `Generated at: ${scaffold.generatedAt}`,
    "",
    "## Summary",
    "",
    `- Platforms: ${scaffold.summary.platforms}`,
    `- Rows: ${scaffold.summary.rows}`,
    `- Notes created: ${scaffold.summary.notesCreated}`,
    `- Notes skipped existing: ${scaffold.summary.notesSkippedExisting}`,
    `- Screenshot TODO files created: ${scaffold.summary.screenshotTodoCreated}`,
    `- Screenshots created: ${scaffold.summary.screenshotsCreated}`,
    ""
  ];
  for (const platform of scaffold.platforms) {
    lines.push(
      `## ${platform.label}`,
      "",
      `- Platform ID: ${platform.id}`,
      `- Evidence root: ${platform.suggestedEvidenceRoot}`,
      `- Rows scaffolded: ${platform.rowsScaffolded}`,
      `- Notes created: ${platform.notesCreated}`,
      `- Screenshot TODO files created: ${platform.screenshotTodoCreated}`,
      ""
    );
    for (const row of platform.rows) {
      lines.push(`- Row ${row.row} ${row.area}: notes=${row.notesPath}; screenshot TODO=${row.screenshotTodoPath}`);
    }
    lines.push("");
  }
  lines.push("## Boundary", "");
  for (const item of scaffold.claimBoundary.doesNotProve) {
    lines.push(`- ${item}`);
  }
  return `${lines.join("\n")}\n`;
}

function buildConsoleSummary(scaffold, outPath, markdownOutPath) {
  return [
    "platform_qa_evidence_scaffold_ok",
    `out=${outPath}`,
    `markdown=${markdownOutPath}`,
    `platforms=${scaffold.summary.platforms}`,
    `rows=${scaffold.summary.rows}`,
    `notesCreated=${scaffold.summary.notesCreated}`,
    `screenshotsCreated=${scaffold.summary.screenshotsCreated}`,
    "claim=false"
  ].join("\n");
}

async function runSelfTest() {
  const selfTestRoot = ".codex-tmp/platform-qa-evidence-scaffold-selftest";
  await mkdir(selfTestRoot, { recursive: true, mode: 0o700 });
  const root = await mkdtemp(join(selfTestRoot, "run-"));
  const handoffPath = join(root, "handoff.json");
  const outPath = join(root, "summary.json");
  const markdownOutPath = join(root, "summary.md");
  const gitHead = "0123456789abcdef0123456789abcdef01234567";
  const handoff = buildSelfTestHandoff(root, gitHead);
  await writePrivateFile(handoffPath, `${JSON.stringify(handoff, null, 2)}\n`);
  const first = await buildPlatformQaEvidenceScaffold(handoffPath);
  await writePrivateFile(outPath, `${JSON.stringify(first, null, 2)}\n`);
  await writePrivateFile(markdownOutPath, buildScaffoldMarkdown(first));
  assert.equal(first.schema, SCAFFOLD_SCHEMA);
  assert.equal(first.canClaimKo, false);
  assert.equal(first.canClaimPlatformQa, false);
  assert.equal(first.summary.rows, 3);
  assert.equal(first.summary.notesCreated, 3);
  assert.equal(first.summary.screenshotsCreated, 0);
  const firstRow = first.platforms[0].rows[0];
  assert.equal(existsSync(firstRow.notesPath), true);
  assert.equal(existsSync(firstRow.screenshotTodoPath), true);
  assert.equal(existsSync(firstRow.screenshotPath), false);
  assert.match(await readFile(firstRow.notesPath, "utf8"), /TEMPLATE ONLY - replace before use/);
  assert.equal((await stat(firstRow.notesPath)).mode & 0o777, 0o600);
  const second = await buildPlatformQaEvidenceScaffold(handoffPath);
  assert.equal(second.summary.notesCreated, 0);
  assert.equal(second.summary.notesSkippedExisting, 3);
  assert.equal(second.summary.screenshotTodoSkippedExisting, 3);
  await assertInvalidPathRejected(root, gitHead);
  await assertSiblingPrefixPathRejected(root, gitHead);
  await assertBackslashPathRejected(root, gitHead);
  console.log("platform_qa_evidence_scaffold_selftest_ok");
}

function buildSelfTestHandoff(root, gitHead) {
  const platformId = "nativeMacManualQa";
  const runName = root.split("/").at(-1) || "run";
  const evidenceRoot = join(SAFE_EVIDENCE_ROOT_PREFIX, "selftest", runName, platformId, gitHead);
  const rowHints = [1, 2, 3].map((row) => {
    const area = `Mac area ${row}`;
    const evidenceDir = join(evidenceRoot, `${String(row).padStart(2, "0")}-mac-area-${row}`);
    return {
      row,
      area,
      evidenceDir,
      suggestedNote: `template only - replace before use: evidence: ${evidenceDir}/notes.md; screenshot: ${evidenceDir}/screenshot.png; result: <actual-result>; observed: <observed-summary>`
    };
  });
  return {
    schema: PLATFORM_QA_HANDOFF_SCHEMA,
    evidenceTier: "PLATFORM_QA_HANDOFF_ONLY",
    canClaimKo: false,
    currentRevision: {
      gitHead
    },
    platforms: [
      {
        id: platformId,
        label: "Native Mac manual QA",
        qaPath: "dist/morning-demo/MAC_MANUAL_QA.md",
        expectedRows: 3,
        suggestedEvidenceRoot: evidenceRoot,
        currentTemplateSummary: {
          rowEvidenceHints: rowHints
        }
      }
    ]
  };
}

async function assertInvalidPathRejected(root, gitHead) {
  const handoff = buildSelfTestHandoff(root, gitHead);
  handoff.platforms[0].currentTemplateSummary.rowEvidenceHints[0].evidenceDir = "../bad";
  const handoffPath = join(root, "bad-handoff.json");
  await writePrivateFile(handoffPath, `${JSON.stringify(handoff, null, 2)}\n`);
  await assert.rejects(
    () => buildPlatformQaEvidenceScaffold(handoffPath),
    /must stay inside/
  );
}

async function assertSiblingPrefixPathRejected(root, gitHead) {
  const handoff = buildSelfTestHandoff(root, gitHead);
  const suggestedRoot = handoff.platforms[0].suggestedEvidenceRoot;
  handoff.platforms[0].currentTemplateSummary.rowEvidenceHints[0].evidenceDir = `${suggestedRoot}-sibling/01-mac-area-1`;
  const handoffPath = join(root, "bad-sibling-handoff.json");
  await writePrivateFile(handoffPath, `${JSON.stringify(handoff, null, 2)}\n`);
  await assert.rejects(
    () => buildPlatformQaEvidenceScaffold(handoffPath),
    /must be under suggestedEvidenceRoot/
  );
}

async function assertBackslashPathRejected(root, gitHead) {
  const handoff = buildSelfTestHandoff(root, gitHead);
  handoff.platforms[0].currentTemplateSummary.rowEvidenceHints[0].evidenceDir =
    ".codex-tmp\\platform-qa-evidence\\bad-windows-path";
  const handoffPath = join(root, "bad-backslash-handoff.json");
  await writePrivateFile(handoffPath, `${JSON.stringify(handoff, null, 2)}\n`);
  await assert.rejects(
    () => buildPlatformQaEvidenceScaffold(handoffPath),
    /must use forward slashes/
  );
}

function assertSafeEvidencePath(value, label) {
  const text = String(value || "");
  assert.ok(text, `${label} must be filled`);
  assert.equal(text.startsWith("/"), false, `${label} must be relative`);
  assert.equal(text.includes("\\"), false, `${label} must use forward slashes`);
  const normalized = text.replaceAll("\\", "/");
  assert.equal(normalized.split("/").includes(".."), false, `${label} must stay inside ${SAFE_EVIDENCE_ROOT_PREFIX}`);
  assert.ok(
    normalized === SAFE_EVIDENCE_ROOT_PREFIX || normalized.startsWith(`${SAFE_EVIDENCE_ROOT_PREFIX}/`),
    `${label} must stay inside ${SAFE_EVIDENCE_ROOT_PREFIX}`
  );
}

function assertPathUnderSuggestedEvidenceRoot(path, suggestedRoot, label) {
  const normalizedPath = normalizeRelativePathForPrefix(path);
  const normalizedRoot = normalizeRelativePathForPrefix(suggestedRoot);
  assert.ok(
    normalizedPath === normalizedRoot || normalizedPath.startsWith(`${normalizedRoot}/`),
    `${label} must be under suggestedEvidenceRoot`
  );
}

function normalizeRelativePathForPrefix(path) {
  return String(path || "").replaceAll("\\", "/").replace(/\/+$/u, "");
}

async function readJson(path, label) {
  try {
    return JSON.parse(await readFile(path, "utf8"));
  } catch (error) {
    throw new Error(`Unable to read ${label} JSON at ${path}: ${error.message}`);
  }
}

async function writePrivateFile(path, content) {
  await mkdir(dirname(path), { recursive: true, mode: 0o700 });
  await chmod(path, 0o600).catch((error) => {
    if (error?.code !== "ENOENT") throw error;
  });
  await writeFile(path, content, { mode: 0o600 });
  await chmod(path, 0o600);
}

async function writePrivateFileIfAbsent(path, content) {
  if (existsSync(path)) {
    return { created: false, skippedExisting: true };
  }
  await writePrivateFile(path, content);
  return { created: true, skippedExisting: false };
}

function parseArgs(argv) {
  const parsed = {};
  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    if (!arg.startsWith("--")) continue;
    const key = arg.slice(2);
    const next = argv[index + 1];
    if (!next || next.startsWith("--")) {
      parsed[key] = true;
      continue;
    }
    parsed[key] = next;
    index += 1;
  }
  return parsed;
}

function buildHelp() {
  return [
    "Usage: node scripts/platform-qa-evidence-scaffold.mjs [options]",
    "",
    "Options:",
    `  --platform-handoff <path>  Platform QA handoff JSON. Default: ${DEFAULT_PLATFORM_HANDOFF_PATH}`,
    `  --out <path>               Summary JSON. Default: ${DEFAULT_OUT_PATH}`,
    `  --markdown-out <path>      Summary Markdown. Default: ${DEFAULT_MARKDOWN_OUT_PATH}`,
    "  --self-test                Run scaffold self-test.",
    "",
    "This creates placeholder notes.md and screenshot.png.TODO.txt files only. It never creates screenshot.png, QA receipts, PASS rows, or release evidence."
  ].join("\n");
}
